public class Operasibilangan {
		//deklarasi attribut dan properti
	  	public int angkaA;
	  	public int angkaB;
	  	private int hasil;
	  	
	  	//method operasi penjumlahan
	  	public void jumlah()
	  	{
	  		this.hasil = this.angkaA + this.angkaB;
	  	}
	  	
	  	//method menampilkan hasil
	  	public void tampilkanhasil()
	  	{
	  		System.out.println("hasil operasi bilangan tersebut adalah: " + this.hasil);
	  	}
}
